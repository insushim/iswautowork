// 성취수준 매핑
function getLevelKey(level: SubjectAchievementLevel): keyof CommonPhrases {
  const mapping: Record<SubjectAchievementLevel, keyof CommonPhrases> = {
    excellent: 'excellent',
    above_average: 'above_average',
    average: 'average',
    below_average: 'below_average',
    needs_improvement: 'needs_improvement',
  };
  return mapping[level];
}

// 공통 문장 랜덤 선택
export function getRandomCommonPhrase(
  subjectName: string,
  grade: number,
  level: SubjectAchievementLevel
): string {
  const gradeGroup = getGradeGroup(grade);
  const levelKey = getLevelKey(level);

  const subjectPhrases = COMMON_PHRASES[subjectName];
  if (!subjectPhrases) {
    // 해당 과목이 없으면 기본 문구 반환
    return '';
  }

  const gradePhrases = subjectPhrases[gradeGroup];
  if (!gradePhrases) {
    return '';
  }

  const phrases = gradePhrases[levelKey];
  if (!phrases || phrases.length === 0) {
    return '';
  }

  // 랜덤 선택
  const randomIndex = Math.floor(Math.random() * phrases.length);
  return phrases[randomIndex];
}

// 모든 공통 문장 가져오기 (프롬프트에 포함용)
export function getAllCommonPhrases(
  subjectName: string,
  grade: number
): Record<string, string[]> {
  const gradeGroup = getGradeGroup(grade);

  const subjectPhrases = COMMON_PHRASES[subjectName];
  if (!subjectPhrases) {
    return {};
  }

  const gradePhrases = subjectPhrases[gradeGroup];
  if (!gradePhrases) {
    return {};
  }

  return {
    '상': gradePhrases.excellent,
    '중상': gradePhrases.above_average,
    '중': gradePhrases.average,
    '중하': gradePhrases.below_average,
    '하': gradePhrases.needs_improvement,
  };
}
